package workingwithinterfaces.myinterfaces.interfaces;

/**
 * Represents a color.
 */
public interface Color {

    /**
     * Returns the color.
     *
     * @return The color.
     */
    String getColor();

    /**
     * Sets the color.
     *
     * @param color The color to set.
     */
    void setColor(String color);

}
